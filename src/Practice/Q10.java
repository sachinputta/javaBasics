package Practice;

/* 10) Write a program to check a given number is even or odd
    i/p n=34
    o/p = 34 is even number
   */

public class Q10 {

	static int n=13;
	
	public static void main(String[] args) {
		
		if(n%2 == 0) {
			System.out.println(n + " is a Even number");
		}
		else {
			System.out.println(n + " is a Odd number");
		}
		
	}
	
}
